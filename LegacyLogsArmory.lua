LLA:RegisterEvent("UNIT_INVENTORY_CHANGED")
LLA:RegisterEvent("RAID_ROSTER_UPDATE")
LLA:RegisterEvent("PARTY_MEMBERS_CHANGED")
LLA:RegisterEvent("COMBAT_LOG_EVENT_UNFILTERED")
