local UIP = UnitIsPlayer
local UN = UnitName
local UL = UnitLevel
local UC = UnitClass
local UR = UnitRace
local US = UnitSex
local UFG = UnitFactionGroup
local UIR = UnitInRaid
local UIC = UnitIsConnected
local REALM = GetRealmName()
local UPVPR = UnitPVPRank
local UPVPN = UnitPVPName
local UIU = UnitIsUnit
local GCAS = GetCurrentArenaSeason

local NeedsUpdate = false
local UpdateTime = 0

function LLA:Print(msg)
	DEFAULT_CHAT_FRAME:AddMessage("LLA: "..msg)
end

function LLA:ParseTitle(title, UName)
	local t = title:match("(.+) "..UName)
	if t then
		return t
	else
		t = title:match(UName..", (.+)")
		if t then
			return t
		else
			return ""
		end
	end
end

function LLA:OnEvent(event, arg1, arg2, arg3, arg4, arg5, arg6, arg7, arg8, arg9, arg10, arg11, arg12, arg13, arg14, arg15)
	if this.loaded and event=="COMBAT_LOG_EVENT_UNFILTERED" then
		if arg2=="SPELL_CAST_START" then
			self:ParseProfessions(arg1, arg3, arg4, arg5, arg6, arg7, arg8, arg9, arg10, arg11)
		end
		return
	end
	if self[event] and this.loaded then
		self.arg1 = arg1
		self.arg2 = arg2
		self[event]()
	else
		self["VARIABLES_LOADED"]()
	end
end

function LLA:GetUnitData(unit, bool)
	local UName = UN(unit)
	local check = GetInventoryItemLink(unit, 5)
	if check and check~="" then
		if not LLADATA[UName] then
			LLADATA[UName] = {}
		end
		local guildName, guildRankName, guildRankIndex = GetGuildInfo(unit)
		local _, raceEn = UR(unit)
		local U = LLADATA[UName]
		local _, unitclass = UC(unit)
		U["season"] = GCAS()
		U[1] = UL(unit) or 70 -- Level
		U[2] = guildName or "" -- Guildname
		U[3] = guildRankIndex or 10 -- GRankIndex
		U[4] = guildRankName or "" -- GRankName
		U[5] = unitclass or ""
		U[6] = raceEn or ""
		U[7] = US(unit) or ""
		U[8] = UFG(unit) or ""
		U[9] = REALM or ""
		for i=1, 19 do
			local ILink = GetInventoryItemLink(unit, i) or ""
			U[9+i] = ILink
		end
		U[29] = LLA:ParseTitle(UPVPN(unit), UName);
		if bool then
			local name, iconTexture, pointsSpent = 0,0,0
			for i=1,3 do
				name, iconTexture, pointsSpent = GetTalentTabInfo(i,true)
				U[29+i] = pointsSpent
			end
			local tkills,thonor,ykills,yhonor,lkills = GetInspectHonorData()
			U[33] = tkills or 0
			U[34] = thonor or 0
			U[35] = ykills or 0
			U[36] = yhonor or 0
			U[37] = lkills or 0
			for i=1,3 do
				local aname,abracket,arating,agames,awins = GetInspectArenaTeamData(i)
				U[38+(i-1)*5] = aname or ""
				U[38+(i-1)*5+1] = abracket or ""
				U[38+(i-1)*5+2] = arating or ""
				U[38+(i-1)*5+3] = agames or ""
				U[38+(i-1)*5+4] = awins or ""
			end
		end
	end
end

function LLA:PLAYER_TARGET_CHANGED()
	if UIU("target", "player") then
		LLA:GetUnitData("player")
		return
	end
	if UIP("target") then
		NotifyInspect("target")
		RequestInspectHonorData()
		for i=1,3 do
			GetInspectArenaTeamData(i)
		end
		NeedsUpdate = true
	end
end

function LLA:UNIT_INVENTORY_CHANGED()
	NotifyInspect(this.arg1)
	this:GetUnitData(this.arg1)
end

function LLA:RAID_ROSTER_UPDATE()
	if UIR("player") then
		for i=1, 40 do
			local UName = UN("raid"..i)
			if not UName then break end
			if UIC("raid"..i) then
				if not LLADATA[UName] then
					NotifyInspect("raid"..i)
					this:GetUnitData("raid"..i)
				end
			end
		end
	end
end

function LLA:PARTY_MEMBERS_CHANGED()
	for i=1, 4 do
		local UName = UN("party"..i)
		if not UName then break end
		if UIC("party"..i) then
			if not LLADATA[UName] then
				NotifyInspect("party"..i)
				this:GetUnitData("party"..i)
			end
		end
	end
end

function LLA:VARIABLES_LOADED()
	if not this.isLoaded then
		if not LLADATA then
			LLADATA = {}
		end
		NotifyInspect("player")
		this:GetUnitData("player")
		this.loaded = true
	end
end

function LLA:OnUpdate(elapsed)
	if NeedsUpdate then
		UpdateTime = UpdateTime + elapsed
		if UpdateTime>=0.5 then
			UpdateTime = 0
			NeedsUpdate = false
			LLA:GetUnitData("target", true)
		end
	end
end

